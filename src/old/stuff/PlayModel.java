package old.stuff;

import java.util.ArrayList;

import models.Ball;
import models.Cloud;
import models.Tee;

public abstract class PlayModel {
	
	private Ball ball;
	private Tee tee;
	private String club;
	private ArrayList<Cloud> clouds;
	private ArrayList<String> clubs;
	
	public Ball getBall() {
		return ball;
	}
	
	public void setBall() {}
	
	public Ball getTee() {
		return ball;
	}
	
	public void setTee() {}
	
	public ArrayList<Cloud> getClouds() {
		return clouds;
	}
	
	public void setClouds(ArrayList<Cloud> clouds) {}
	
	public ArrayList<String> getClubs() {
		return clubs;
	}
	
	public void setClub(String aClub) {}
}

